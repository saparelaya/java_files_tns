package org.tns.capgemini.c2tc.java;

public class TypeCasting {

	public static void main(String[] args) {
		        //implicit casting
				byte b=123;
				int i=b;
				System.out.println(i);
				
				float f=36.53f;
				double d=f;
				System.out.println(d);
				
				//explicit writing
				 
				double f1=76.35d;
				long l=(long)f1;
				System.out.println(l);
				
				float f2=876.39f;
				int li=(int)f2;
				System.out.println(li);

	}

}
