package org.tns.capgemini.c2tc.java;

public class AssignmentOperator {

	public static void main(String[] args) {
		    int a=25;
			
			
			a+=15; 
			System.out.println("a after += :" +a);
			
			a-=5; 
			System.out.println("a after -= :"  +a);
			
			a*=10;  
			System.out.println("a after *= :"  +a);
			
			a/=40;
			System.out.println("a after /= :" +a);
			
			a%=6; 
			System.out.println("a after %= :" +a);


	}

}
